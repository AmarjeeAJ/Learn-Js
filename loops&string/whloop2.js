// while loop with continue statement
let i = 1;
while (i < 10) {
  if (i == 5) continue;
  
  else console.log(i);
  i++;
}

// continue statement skip the current iteration so it will not print 5 and continue the next iteration
// Output
// 1
// 2
// 3
// 4
// 6
